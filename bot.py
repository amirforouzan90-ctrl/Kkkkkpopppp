def search_prompt(message):
    msg = bot.send_message(message.chat.id, "نام بیماری را وارد کنید:")
    bot.register_next_step_handler(msg, search_disease)


def search_disease(message):
    query = message.text.strip()
    found = [name for name in DISEASES.keys() if query in name]
    if found:
        markup = types.InlineKeyboardMarkup()
        for name in found:
            markup.add(types.InlineKeyboardButton(name, callback_data=f"disease_{name}"))
        bot.send_message(message.chat.id, "بیماری‌های یافت شده:", reply_markup=markup)
    else:
        bot.send_message(message.chat.id, f"بیماری‌ای با نام '{query}' یافت نشد.")


